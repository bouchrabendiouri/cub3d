#include "cub3D.h"


int check_name_cub(char *str) {
    int i = ft_strlen(str);
    if (i < 5)
        return (1);
    i--;
    if (str[i -1] == 'b' && str[i - 2] == 'u' && str[i - 3] == 'c'
        && str[i - 4] == '.' && str[i - 5] && str[i - 5] != '/')
        return (0);
    return (-1);
}
